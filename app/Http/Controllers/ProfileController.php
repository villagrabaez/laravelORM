    /**
     * Display the specified resource.
     *
     * @param  \App\Profile  $profile
     * @return \Illuminate\Http\Response
     */
    public function show(Profile $profile)
    {
        // optimizar las consultas con with, with('category', 'image', 'tags').
        $user = User::find($profile->user_id);
        $posts = $user->posts()
            ->with('category', 'image', 'tags')
            ->withCount('comments')
            ->get();
        $videos = $user->videos()
            ->with('category', 'image', 'tags')
            ->withCount('comments')
            ->get();
        return view('profile.show', compact('user', 'posts', 'videos'));
    }

